    private void actualizarPeliculas() {
  // Obtener todas las salas del cine
    ArrayList<Sala> salas = this.cineSergio.getSalas();

    // Limpiar todos los elementos del JComboBox
    this.jComboBoxPeliculas.removeAllItems();
    this.jComboBoxComprarEntrada.removeAllItems();
    // Iterar sobre todas las salas del cine
    for (Sala salita : salas) {
        // Obtener la película de la sala actual
        Pelicula pelicula = salita.getPelicula();
        
        // Obtener el título de la película y agregarlo al JComboBox
        String tituloPelicula = pelicula.getTitulo();
        this.jComboBoxPeliculas.addItem(tituloPelicula);
        this.jComboBoxComprarEntrada.addItem(tituloPelicula);
    }

    }

